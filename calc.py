# 더하기 함수
def sum(num1, num2):
    return num1 + num2


# 빼기 함수
def sub(num1, num2):
    return num1 - num2


# 나누기 함수
def div(num1, num2):
    if num2 == 0:
        return 0
    return num1 / num2


# 곱하기 함수
def mul(num1, num2):
    return num1 * num2


# 제곱 함수
def square(a):
    return a**2


# 나머지 함수
def mod(a, b):
    return a % b
